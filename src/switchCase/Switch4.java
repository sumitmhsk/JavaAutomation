package switchCase;

public class Switch4 {

	public static void main(String[] args) {
		char ch = 'p';
		switch (ch) {
		case 'a':
		System.out.println("The given character is a vowel");
		break;
		
		case 'e':
			System.out.println("The given character is a vowel");
			break;
			
		case 'i':
			System.out.println("The given character is a vowel");
			break;
			
		case 'o':
			System.out.println("The given character is a vowel");
			break;
			
		case 'u':
			System.out.println("The given character is a vowel");
			break;
			
		default:
			System.out.println("The given character is a consonant");
			break;
		
		}

	}

}
