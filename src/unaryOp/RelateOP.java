package unaryOp;

public class RelateOP {

	public static void main(String[] args) {

		int num1 = 15, num2 = 20;
		System.out.println("num1: " + num1 + "num2: " + num2);
        // 12 4
		System.out.println("num1 == num2 " + (num1 == num2));//false
       // is equal to
		System.out.println("num1 != num2 " + (num1 != num2));//true
       // is not equal to
		System.out.println("num1 > num2 " + (num1 > num2));//false
       // greater than
		System.out.println("num1 < num2 " + (num1 < num2));// true
       // smaller than
		System.out.println("num1 >= num2 " + (num1 >= num2));//false
      // greater or equal to
		System.out.println("num1 <= num2 " + (num1 <= num2));//true
	// smaller or equal to
	}

}
